#include<iostream>
using namespace std;

int Insertion_Sort() {
    int n, v, j;
    cin >> n;
    int array[n];
    for (int i = 0; i < n; i++) {
        cin >> array[i];
    }
   
    
    for (int i = 0; i < n; i++) {
        v = array[i];
        j = i - 1;
    }
    
    return 0;
}
